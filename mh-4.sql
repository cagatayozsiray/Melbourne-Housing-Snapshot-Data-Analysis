-- Mülkün bulunduðu mahallenin mülk fiyat aralýklarýna göre adetini gösteren sorgu

SELECT 
    Suburb,
    SUM(CASE WHEN Price BETWEEN 85000 AND 685000 THEN 1 ELSE 0 END) AS '85.000 - 685.000',
    SUM(CASE WHEN Price BETWEEN 685001 AND 1285001 THEN 1 ELSE 0 END) AS '685.001 - 1.285.001',
    SUM(CASE WHEN Price BETWEEN 1285002 AND 1885002 THEN 1 ELSE 0 END) AS '1.285.002 - 1.885.002',
    SUM(CASE WHEN Price BETWEEN 1885003 AND 2485003 THEN 1 ELSE 0 END) AS '1.885.003 - 2.485.003',
	SUM(CASE WHEN Price BETWEEN 2485004  AND 3085004 THEN 1 ELSE 0 END) AS '2.485.004 - 3.085.004'
FROM 
    melbourne_housing
GROUP BY 
    Suburb
ORDER BY Suburb

